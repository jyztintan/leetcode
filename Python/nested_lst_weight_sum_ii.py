class Solution:
    def depthSumInverse(self, nestedList: List[NestedInteger]) -> int:
        integers_depth = []
        max_depth = [0]
        q = []

        def traverse(curr, depth):
            if curr.isInteger():
                integers_depth.append((curr.getInteger(), depth))
                max_depth[0] = max(max_depth[0], depth)
                return
            lst = curr.getList()
            for num in lst:
                traverse(num, depth + 1)

        for num in nestedList:
            traverse(num, 1)

        total = 0
        for num, depth in integers_depth:
            total += num * (max_depth[0] - depth + 1)
        return total

